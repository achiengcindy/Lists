squares = []
for x in range(1, 11):
    square = x**2
    squares.append(square)
print(squares)

# covert a list to Upper Case
names = ['kai', 'abe', 'ada', 'gus', 'zoe']
upper_names = []
for name in names:
    upper_names.append(name.upper())
print(upper_names)

# print the letters in the word 'python'
name_letters = []
for name in 'Python':
    name_letters.append(name)
